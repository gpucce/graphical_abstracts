function draw_tag(p, w, h; n_w=40, tag="NOUN", color="red")
	current_color = getcolor()
	setcolor(color)
	box(p, w, h, :stroke)
	n_h = 15
	# box(p + Point(0, -h / 2 - n_h / 2), 4, n_h / 2, :fill)
	p2 = p + Point(-(w - n_w) / 2, - (h + n_h) / 2)
	box(p2, n_w, n_h, :fillstroke)
	setcolor("black")
	fontsize(12)
	text(tag, p2, halign=:center, valign=:middle)
	setcolor(current_color)
end

